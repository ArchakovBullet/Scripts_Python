a = 8
b = 3

print('Сложение:\t\t',a,'+',b,'=',a+b)
print('Вычитание:\t\t',a,'-',b,'=',a-b)
print('Умножение:\t\t',a,'*',b,'=',a*b)
print('Деление:\t\t',a,'/',b,'=',a/b)
print('Целочисленное деление:\t\t',a,'//',b,'=',a//b)

# Визуально с sep или без sep никакой разницы.
print('Возведение в степень:\t\t',a,' в степени',b,'=',a**b,sep=' ')
